#Örnek2: Veriyi kullanıcıdan alacak şekilde 2 sayı oluşturun. Daha sonra toplama,çıkarma,çarpma,bölme,üs alma (kuvvet) ve mod alma programı yazalım.
sayi1: float(input("Bir sayı giriniz: "))
sayi2: float(input("Bir sayı giriniz: "))

